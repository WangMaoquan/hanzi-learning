// 简繁转换函数
export function toSimplified(str: string): string {
  let result = "";
  for (const char of str) {
    result += MANUAL_T2S[char] || char;
  }
  return result;
}

// 朝代映射
const DYNASTY_MAP: Record<string, string> = {
  唐: "tang",
  宋: "song",
  元: "yuan",
  明: "ming",
  清: "qing",
  汉: "han",
  魏: "wei",
  晋: "jin",
  秦: "qin",
  楚: "chu",
  隋: "sui",
  五代: "wudai",
};

export function getDynastyEn(dynasty: string): string {
  return DYNASTY_MAP[dynasty] || dynasty;
}

// 估算难度（根据诗句总字数）
export function estimateDifficulty(verses: string[]): number {
  const totalChars = verses.join("").length;
  if (totalChars <= 20) return 1; // 绝句
  if (totalChars <= 28) return 2; // 律诗
  if (totalChars <= 40) return 3; // 词
  if (totalChars <= 60) return 4; // 长词/排律
  return 5; // 长诗
}

// 生成拼音
export function getPinyin(text: string): string {
  try {
    const result = pinyin(text, { style: "tone" });
    return result.map((arr) => arr[0]).join(" ");
  } catch {
    return "";
  }
}

// 生成逐字拼音数组
export function getVersePinyins(verse: string): string[] {
  try {
    const result = pinyin(verse, { style: "tone" });
    return result.map((arr) => arr[0]);
  } catch {
    return [];
  }
}

// 生成标签
export function generateTags(dynasty: string, _author: string): string[] {
  return [dynasty + "诗"];
}
